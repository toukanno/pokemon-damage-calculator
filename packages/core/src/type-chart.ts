/**
 * タイプ相性倍率を取得する
 */
export function getTypeEffectiveness(
  attackType: PokemonType,
  defenseType: PokemonType,
): number {
  return typeChart[attackType]?.[defenseType] ?? 1.0;
}

/**
 * 複合タイプへのタイプ相性倍率を取得する
 */
export function getTypeEffectivenessMulti(
  attackType: PokemonType,
  defenseTypes: PokemonType[],
): number {
  let multiplier = 1.0;
  for (const defType of defenseTypes) {
    multiplier *= getTypeEffectiveness(attackType, defType);
  }
  return multiplier;
}
